char	*ft_strrchr(const char *s, int c)
{	
	char	*buf;

	buf = 0;
	while (*s)
	{
		if (*s == (char)c)
			buf = (char *)s;
		s++;
	}
	if (*s == '\0' && (char)c == '\0')
		return ((char *)s);
	return (buf);
}
